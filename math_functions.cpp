#include<iostream>
#include<cmath>
using namespace std;

int main(){
    int d = 10;

    cout << "The value of the variable d is : " << d << endl;
    cout << "sin(d) : " << sin(d) << endl;
    cout << "cos(d) : " << cos(d) << endl;
    cout << "tan(d) : " << tan(d) << endl;
    cout << "log(d) : " << log(d) << endl;
    cout << "pow(d,2) : " << pow(d,2) <<endl;
    cout << "sqrt(d) : " << sqrt(d) << endl;
}
